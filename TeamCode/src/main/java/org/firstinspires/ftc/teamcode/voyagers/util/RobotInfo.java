public class RobotInfo
{
	public final float length;
	public final float width;
	public final float height;
	public final float phoneCamX;
	public final float phoneCamY;
	public final float phoneCamZ;

	public RobotInfo(float length, float width, float height, float phoneCamX, float phoneCamY, float phoneCamZ)
	{
		this.length = length;
		this.width = width;
		this.height = height;
		this.phoneCamX = phoneCamX;
		this.phoneCamY = phoneCamY;
		this.phoneCamZ = phoneCamZ;
	}
}
